/*************************************************************
 * 采用位运算以及不使用分支控制语句求一个整数的绝对值       **
 * 关键点：一个数与 0x00000000 异或之后不变，而与 oxfffffff**
 * 异或之后相当于求反                                      **
 * **********************************************************/
int abs(int val) {
	int temp = val;
	int out = val;

	temp = temp >> 31;
	out = out ^ temp;
	out = out - temp;

	return out;
}
